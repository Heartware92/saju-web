/**
 * subItem 또는 customLabel·category 로 가장 정확한 힌트 1개를 선택.
 */
export function pickTaekilDetailHint(
  category: string | null,
  subItem: string | null,
): string {
  if (subItem && TAEKIL_DETAIL_HINTS[subItem]) return TAEKIL_DETAIL_HINTS[subItem];
  if (category && TAEKIL_CATEGORY_HINTS[category]) return TAEKIL_CATEGORY_HINTS[category];
  return '어떤 상황인가요? 자세한 정황을 적어주시면 더 정확한 풀이를 받을 수 있어요.';
}

