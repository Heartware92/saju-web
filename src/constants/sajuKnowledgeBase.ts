/**
 * customHobby 자유 입력 → 11분야 정규화.
 * 11분야 정확 매칭이면 그대로, 키워드 포함되면 해당 분야로, 둘 다 안 되면 null.
 */
export function normalizeHobbyToCategory(input: string | undefined | null): string | null {
  if (!input) return null;
  const trimmed = input.trim();
  if (!trimmed) return null;

  // 11분야 정확 일치
  const exactCategories = HOBBY_CATEGORY_KEYWORDS.map(([cat]) => cat);
  if (exactCategories.includes(trimmed)) return trimmed;

  // 키워드 부분 매칭
  for (const [cat, keywords] of HOBBY_CATEGORY_KEYWORDS) {
    if (keywords.some((kw) => trimmed.includes(kw))) return cat;
  }
  return null;
}

