export function classifyAnswer(ans: string | undefined | null): AnswerGroup[] {
  if (!ans) return [];
  const trimmed = ans.trim();
  if (!trimmed) return [];
  const groups: AnswerGroup[] = [];
  (Object.keys(ANSWER_KEYWORD_GROUPS) as Array<Exclude<AnswerGroup, 'other'>>).forEach((g) => {
    if (ANSWER_KEYWORD_GROUPS[g].some((kw) => trimmed.includes(kw))) groups.push(g);
  });
  return groups.length > 0 ? groups : ['other'];
}

