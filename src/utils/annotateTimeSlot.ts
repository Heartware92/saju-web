// 12지지(한자 시진) 한글 → 실제 시간 매핑.
// 사용자가 "진시"·"진·미시" 같은 한자 시진만 봤을 때 시간을 모르는 케이스 해결.
const ZHI_HOUR: Record<string, string> = {
  '자': '23-01시', '축': '01-03시', '인': '03-05시', '묘': '05-07시',
  '진': '07-09시', '사': '09-11시', '오': '11-13시', '미': '13-15시',
  '신': '15-17시', '유': '17-19시', '술': '19-21시', '해': '21-23시',
};

/**
 * timeSlot 문자열의 한자 시진 패턴에 실제 시간 병기.
 * - "진시" → "진시 (07-09시)"
 * - "진·미시" → "진시 (07-09시) · 미시 (13-15시)"
 * - "진·술·축·미시" → "진시 (07-09시) · 술시 (19-21시) · 축시 (01-03시) · 미시 (13-15시)"
 * - 이미 숫자 시간이 포함되어 있으면("오전 5~7시 (인·묘시)") 그대로 반환.
 */
export function annotateTimeSlot(s: string | undefined | null): string {
  if (!s) return '—';
  if (/\d{1,2}/.test(s)) return s;
  // "X·Y·Z·...시" → 각 한자를 개별 "X시 (시간)" 으로 분해
  const m = s.match(/^([자축인묘진사오미신유술해])((?:·[자축인묘진사오미신유술해])+)?시$/);
  if (m) {
    const heads: string[] = [m[1]];
    if (m[2]) {
      for (const c of m[2].split('·')) if (c) heads.push(c);
    }
    return heads
      .map((h) => `${h}시 (${ZHI_HOUR[h] ?? ''})`)
      .join(' · ');
  }
  // 일반 케이스: 한자 시진 패턴이 문장 안에 섞여 있을 때 단일 매칭으로 병기
  return s.replace(/([자축인묘진사오미신유술해])시/g, (_m, a) => `${a}시 (${ZHI_HOUR[a] ?? ''})`);
}
